<h1>From the Ancient Manuscripts of Lore</h1>


<p>Before the time of the shaking, before the tainting of mortal blood, and before the beginning of everything, there was The Continuum. The swirl of the Aether oversaw the balance, and Order and Chaos, Light and Darkness flowed never-ending over the void. The gods of these domains, Astra and Nox sat in Divine Council.</p>

<p>The two opposing forces were in constant strife, and the clashing caused all of existences to tremble from the colliding powers. From this shaking sprang earth, water, the moon and time. And so Jirhai, Sela, Nyx and Chron took their places in the Divine Council and balance existed.</p>

<p>After a time, the gods decided to create mortal beings that were like them to fill the waters and the earth and who would worship them. Jirhai bore those creations who walked upon the earth, Sela birthed those who swam in her waters, and Astra gave life to those who called the skies home. From this birth, mortal life flourished, and with it Death, for immortality could be the mana of the gods alone. The goddess Anima was their blessing, a blissful guide to beginning again when each journey in mortality was done. Each god favored their creations, and those who had not created, adopted some their own, bestowing upon some of them beauty, strength, agility, speed and reason.</p>

<p>For a time, there was harmony between the planes, until, it is told, Nox instigated the mortals to an uprising against their creators. Born of a forbidden love between the goddess Nyx and her royal human consort, the ruler of the first great  Miritonian  kingdom of Ankara, the scheme of Chaos tempted mortals and the goddess alike, to believe that they could successfully defy the divine established Order that forbade their union. The pride of the created, mirrored that of their creators, and thus the Mortal War began. But what mortals could hope to defeat the gods themselves? As quickly as the rebellions had begun, they were tempered, and the kingdom that had dared to defy was swallowed into the sands, cursed and imprisoned ever more. The blood and death suffered by mortals at the hands of the gods was only the beginning of their punishment for their crime against their creators.</p>

<p>As penance for their rebellion, Earth gave birth to Wraith, the original carrier of the vampiric and lycanthropic curses. War emerged in the form of Seria to incite fighting amongst the races. Zagreus was born, bringing with him mortal indulgence and emotion to cloud their resolve. The goddess Nyx was stripped of her ability to feel emotion, and her corporeal body shattered and scattered to the worlds' ends. These were but the beginning of horrors, for the worst punishment to endure was the most lasting. The blissful hope that was Death; the river Anima and the rebirth into the Continuum, was taken from the mortals, and replaced with the dark imprisonment of a never ending Underworld. Unable to bear the suffering of the mortals, the goddess of Death chose to sacrifice her own existence, taking all who were trapped in the Underworld with her, in her own return to the Continuum. Death was gone, but the Underworld remained, and there the orderless tumult of mortality in a void remained without dominion, until the ascension of Diiroehn, the grim; the new Death.</p>

<p>From these punishments, the mortals spiraled into a Dark Age. Curses ran rampant like plagues, War broke out between them, spurred on by greed and power lust, and their numbers began to decline to a near extinction.</p>

<p>The gods reconvened at the seats of the Divine Council to discuss ending the suffering of the mortals who still remained. In the end, it was decided that the sentence had been served, and so the gods gave the mortals gifts. Nyx was empowered to give the mortals dreams, as an escape from their suffering and a reprieve from her own inability to experience emotion by tasting of theirs. Valdek was birthed to bestow Craft upon them, and the freedom to express what was left of beauty in their souls. Meran was given the status of a god, and the mortals were gifted knowledge and Lore.</p>

<p>Thus the gods were celebrated again for ending the dark ages. Jirhai, Sela and Nox were given watch over the Terrestrial Plane, while the gifts and curses of the gods remained within the mortal sphere. Astra, Chron and Nyx remained in the Aether to watch over the heavens. Diiroehn having ascended to godhood as the new god of Death brought order to the Underworld. There has been a delicate balance existing between the gods and their creations, ever since.</p>

<p>How long it shall remain is a tale yet to be told.</p>

